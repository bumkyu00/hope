def build_train_data(dreams_per_fact):
    """Build training QA with think retrieval chains, limited to N dreams per fact."""
    data = []
    for fact_key, dreams in DREAM_POOL.items():
        selected = dreams[:dreams_per_fact]
        fact_text = FACTS[fact_key]["fact"]
        for query, think, response in selected:
            data.append({
                "q": query,
                "a": f"<think>\n{think}\n</think>\n{response}",
            })
    return data

def build_test_data():
    """Build flat test list."""
    tests = []
    for category, scenarios in TEST_SCENARIOS.items():
        for q, keywords in scenarios:
            tests.append({
                "q": q,
                "should_remember": keywords,
                "category": category,
                "description": f"{category}: {q[:30]}",
            })
    return tests

